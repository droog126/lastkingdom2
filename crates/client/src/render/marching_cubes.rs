/// MC 输出顶点
#[derive(Debug, Clone, Copy)]
pub struct McVertex {
    pub position: [f32; 3],
    pub normal: [f32; 3],
}

/// 从 ScalarField 抽 mesh
///
/// `iso` 通常 0.5（sdf 一半位置）  
/// `corner_origin` = 角点 (0,0,0) 对应的世界坐标  
/// `cell_size` = 每 cell 1m（默认 1.0）
pub fn build_mesh(
    field: &ScalarField,
    iso: f32,
    corner_origin: [f32; 3],
    cell_size: [f32; 3],
) -> (Vec<McVertex>, Vec<u32>) {
    let mut vertices: Vec<McVertex> = Vec::new();
    let mut indices: Vec<u32> = Vec::new();

    let sx = field.shape[0];
    let sy = field.shape[1];
    let sz = field.shape[2];
    // cell 数 = 角点数 - 1
    if sx < 2 || sy < 2 || sz < 2 {
        return (vertices, indices);
    }
    let nx = sx - 1;
    let ny = sy - 1;
    let nz = sz - 1;

    // 角点 lookup: idx = (sy * sz + z) * sx + x
    let _idx = |x: usize, y: usize, z: usize| -> usize { (y * sz + z) * sx + x };

    // 临时缓存：12 edge 的交点（每个 cell 重用一次）
    let mut vert_list: [[f32; 3]; 12] = [[0.0; 3]; 12];

    for z in 0..nz {
        for y in 0..ny {
            for x in 0..nx {
                // 8 角点 density
                let d = [
                    field.get(x, y, z),
                    field.get(x + 1, y, z),
                    field.get(x + 1, y, z + 1),
                    field.get(x, y, z + 1),
                    field.get(x, y + 1, z),
                    field.get(x + 1, y + 1, z),
                    field.get(x + 1, y + 1, z + 1),
                    field.get(x, y + 1, z + 1),
                ];

                // case index
                let mut case: usize = 0;
                if d[0] < iso {
                    case |= 1;
                }
                if d[1] < iso {
                    case |= 2;
                }
                if d[2] < iso {
                    case |= 4;
                }
                if d[3] < iso {
                    case |= 8;
                }
                if d[4] < iso {
                    case |= 16;
                }
                if d[5] < iso {
                    case |= 32;
                }
                if d[6] < iso {
                    case |= 64;
                }
                if d[7] < iso {
                    case |= 128;
                }

                if case == 0 || case == 255 {
                    continue; // 全空或全实 → 无交点
                }

                // 8 角点的世界坐标
                let p = |lx: usize, ly: usize, lz: usize| -> [f32; 3] {
                    [
                        corner_origin[0] + (x + lx) as f32 * cell_size[0],
                        corner_origin[1] + (y + ly) as f32 * cell_size[1],
                        corner_origin[2] + (z + lz) as f32 * cell_size[2],
                    ]
                };
                let p0 = p(0, 0, 0);
                let p1 = p(1, 0, 0);
                let p2 = p(1, 0, 1);
                let p3 = p(0, 0, 1);
                let p4 = p(0, 1, 0);
                let p5 = p(1, 1, 0);
                let p6 = p(1, 1, 1);
                let p7 = p(0, 1, 1);

                // 12 edge 的端点对（按 MC 标准 edge order）
                let edge_ends: [[usize; 2]; 12] = [
                    [0, 1],
                    [1, 2],
                    [2, 3],
                    [3, 0],
                    [4, 5],
                    [5, 6],
                    [6, 7],
                    [7, 4],
                    [0, 4],
                    [1, 5],
                    [2, 6],
                    [3, 7],
                ];
                let edge_pts: [[f32; 3]; 8] = [p0, p1, p2, p3, p4, p5, p6, p7];

                // 计算此 cell 用到的 edge 交点
                let edge_mask = EDGE_TABLE[case];
                for i in 0..12 {
                    if (edge_mask & (1 << i)) == 0 {
                        continue;
                    }
                    let [a, b] = edge_ends[i];
                    let pa = edge_pts[a];
                    let pb = edge_pts[b];
                    let da = d[a];
                    let db = d[b];
                    // 沿 edge 线性插值求 iso 交点
                    let t = if (da - db).abs() < 1e-6 {
                        0.5
                    } else {
                        (iso - da) / (db - da)
                    }
                    .clamp(0.0, 1.0);
                    let pt = [
                        pa[0] + t * (pb[0] - pa[0]),
                        pa[1] + t * (pb[1] - pa[1]),
                        pa[2] + t * (pb[2] - pa[2]),
                    ];
                    vert_list[i] = pt;
                }

                // 按 tri_table 写入三角形
                let tri_row = &TRI_TABLE[case];
                let mut k = 0;
                while k < 16 && tri_row[k] != -1 {
                    let base = vertices.len() as u32;
                    let e0 = tri_row[k] as usize;
                    let e1 = tri_row[k + 1] as usize;
                    let e2 = tri_row[k + 2] as usize;
                    // 三个 edge 交点 → 三个顶点
                    let pts = [vert_list[e0], vert_list[e1], vert_list[e2]];
                    // 法线 = 三角形右手叉积
                    let e1v = [
                        pts[1][0] - pts[0][0],
                        pts[1][1] - pts[0][1],
                        pts[1][2] - pts[0][2],
                    ];
                    let e2v = [
                        pts[2][0] - pts[0][0],
                        pts[2][1] - pts[0][1],
                        pts[2][2] - pts[0][2],
                    ];
                    let n = cross(e1v, e2v);
                    let n = normalize(n);
                    for pt in &pts {
                        vertices.push(McVertex { position: *pt, normal: n });
                    }
                    indices.push(base);
                    indices.push(base + 1);
                    indices.push(base + 2);
                    k += 3;
                }
            }
        }
    }

    // 法线平滑：把每个顶点的法线改成"邻接三角形法线平均"（共享顶点天然共享法线）
    // 当前每个顶点都是独立的，没共享，所以需要做合并
    // 简化：第一版跳过这个；如果要法线平滑，后续做一个 hash 合并阶段

    (vertices, indices)
}

#[inline]
fn cross(a: [f32; 3], b: [f32; 3]) -> [f32; 3] {
    [
        a[1] * b[2] - a[2] * b[1],
        a[2] * b[0] - a[0] * b[2],
        a[0] * b[1] - a[1] * b[0],
    ]
}

#[inline]
fn normalize(v: [f32; 3]) -> [f32; 3] {
    let len = (v[0] * v[0] + v[1] * v[1] + v[2] * v[2]).sqrt();
    if len < 1e-6 {
        [0.0, 1.0, 0.0]
    } else {
        [v[0] / len, v[1] / len, v[2] / len]
    }
}

