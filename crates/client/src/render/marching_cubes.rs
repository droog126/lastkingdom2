        // case 1: 角点 0 实，其余空 → 1 三角形（3 顶点, 3 indices）
        let mut field = ScalarField { data: vec![0.0; 8], shape: [2, 2, 2], origin: [0, 0, 0] };
        // 角点顺序 (按代码): 0=(0,0,0) 1=(1,0,0) 2=(1,0,1) 3=(0,0,1)
